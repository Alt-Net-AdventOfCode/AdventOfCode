        public override object GiveAnswer1()
        {
            var result = string.Empty;
            for (var i = 0; i < _lines[0].Length; i++)
            {
                var hit = new Dictionary<char, int>();
                foreach (var line in _lines)
                {
                    if (!hit.ContainsKey(line[i]))
                    {
                        hit[line[i]] = 1;
                    }
                    else
                    {
                        hit[line[i]]++;
                    }
                }

                var status = (0, ' ');
                foreach (var entry in hit)
                {
                    if (entry.Value > status.Item1)
                    {
                        status.Item1 = entry.Value;
                        status.Item2 = entry.Key;
                    }
                }

                result += status.Item2;
            }

            return result;
        }
